package com.cloudformica.devops.MathsLib;

public class Operations {
	static double add(double no1, double no2)
	{
		return no1+no2;	
	}
	static double sub(double no1, double no2)
	{
		return no1-no2;	
	}
	static double mult(double no1, double no2)
	{
		return no1*no2;	
	}
	static double div(double no1, double no2)
	{
		return no1/no2;	
	}

}
